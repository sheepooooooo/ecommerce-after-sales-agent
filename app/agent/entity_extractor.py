import re


ORDER_ID_PATTERN = re.compile(r"\b(ord\d{5})\b", re.IGNORECASE)


def extract_order_ids(user_query: str) -> list[str]:
    """
    提取问题中所有形如 ORD10003 的订单号，并统一标准化为大写。

    当前阶段订单号格式非常稳定：固定前缀 ORD 加 5 位数字。因此先用正则表达式
    是更可控、更可测试的方案。它不会因为自然语言上下文变化而产生“猜测式”结果。

    这里不使用 LLM 提取订单号，是为了避免把本来确定性的标识符识别变成概率问题。
    订单号一旦识别错误，后续可能查询到错误订单或创建错误工单，风险高于收益。

    后续如果要提取更复杂的实体，例如用户描述的故障现象、期望处理方式、时间范围、
    多轮对话中的省略指代，再考虑让 LLM 在受约束 schema 下辅助抽取。
    """
    if not user_query:
        return []

    matches = ORDER_ID_PATTERN.findall(user_query)
    return [match.upper() for match in matches]


def extract_order_id(user_query: str) -> str | None:
    """
    提取第一个订单号。

    一个问题里出现多个订单号时，Agent 默认只选择第一个，同时由图节点把全部匹配结果
    记录到 debug 中。这样不替用户猜测“真正想查哪一单”，也方便后续追问澄清。
    """
    order_ids = extract_order_ids(user_query)
    if not order_ids:
        return None
    return order_ids[0]

